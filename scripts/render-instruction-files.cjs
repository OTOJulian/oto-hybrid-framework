'use strict';

const fs = require('node:fs');
const path = require('node:path');
const { render } = require('../bin/lib/instruction-file.cjs');
const { version: OTO_VERSION } = require('../package.json');

const repoRoot = path.resolve(__dirname, '..');
const ctx = {
  otoVersion: OTO_VERSION,
  repoUrl: 'https://github.com/OTOJulian/oto-hybrid-framework',
  generatedHeader: '<!-- Generated by scripts/render-instruction-files.cjs from oto/templates/instruction-file.md. Do not edit. -->',
};

const writes = [
  ['CLAUDE.md', 'claude'],
  ['AGENTS.md', 'codex'],
  ['GEMINI.md', 'gemini'],
];

for (const [outFile, runtime] of writes) {
  const out = render(runtime, ctx);
  fs.writeFileSync(path.join(repoRoot, outFile), out);
  process.stdout.write(`wrote ${outFile} (${out.length} bytes, runtime=${runtime})\n`);
}
